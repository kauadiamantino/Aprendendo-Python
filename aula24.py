'''
Fatiamento de strings
012345678
Olá mundo
-987654321
Fatiamento [i:f:p] [::]
Obs.: a função len retorna a qtd
de caracteres da str
'''
variavel = 'Olá mundo'

print(variavel [5])
print(variavel [-4])
print(variavel [4:7])
print(variavel [4:])
print(variavel [0:4])
print(10 * '-')
print(variavel [:6])
print(variavel [3])
print(len(variavel))
print(len(variavel [-2]))
print(variavel[0:9:2])
print(variavel[0:9:3])
print(variavel[0:9:1])
print(variavel[::-1])
print(variavel[-1:-10:-1])
